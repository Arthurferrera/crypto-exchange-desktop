package br.unip.model;

public class Dashboard {

	private String nomeUsuario;
	private String qtdClientes;
	private String qtdOperacoes;
	private String valorOperacoes;
	
	public String getNomeUsuario() {
		return nomeUsuario;
	}
	public void setNomeUsuario(String nomeUsuario) {
		this.nomeUsuario = nomeUsuario;
	}
	public String getQtdClientes() {
		return qtdClientes;
	}
	public void setQtdClientes(String qtdClientes) {
		this.qtdClientes = qtdClientes;
	}
	public String getQtdOperacoes() {
		return qtdOperacoes;
	}
	public void setQtdOperacoes(String qtdOperacoes) {
		this.qtdOperacoes = qtdOperacoes;
	}
	public String getValorOperacoes() {
		return valorOperacoes;
	}
	public void setValorOperacoes(String valorOperacoes) {
		this.valorOperacoes = valorOperacoes;
	}
	
	
	
}
